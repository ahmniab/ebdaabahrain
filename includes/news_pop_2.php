			  <p>Ebdaa Bank for Microfinance has signed an agreement with the Shariyah Review Bureau (SRB) in Bahrain to ensure that the Bank's financial products comply with Shariah requirements.</p>
			  <p>Chief Executive of Ebdaa Bank, Dr. Khalid Al Ghazzawi stated that the agreement will further enhance the Bank's ability to adopt the best practices in this field. “This agreement is in line with the Bank's initiative to expand its range of financial products to not only include loans for low-income and micro-entrepreneurs, but also to those looking for Shariah-compliant financing,” sair Dr. Al Ghazzawi.</p>
			  <p>"More attention will be paid to the design of Shariah-compliant products that meet the expectations of our clients, with due regard to the proper application of Shariah principles and guidelines to microfinance operations," he added.  </p>
			  <p>Although Ebdaa Bank will be extending its range of services to include Shariah- compliant financing, it will continue to provide traditional loans to customers seeking conventional financing.</p>
			  <p>Operations Manager of Ebdaa Bank, Ghaith Al Moneim, stated that the Bank will work with SRB to implement a range of ambitious Islamic financial initiatives. "These guidelines will not only be in line with Shariah principles, but also with the Bank's vision and objective to support low income individuals,” said Mr. Al Moneim.</p>
			  <p>“We also intend to continue to assist them in their transition to self-employment and ensure their sustainability and prosperity, through the development of their businesses and projects,” he added.</p>
			  <p>Chief Executive Officer of SRB, Yasir Dahlawi, stated that he agreement with Ebda Bank for Microfinance supports the joint approach of the two parties towards the development of the Kingdom’s society and economy. “This agreement is also in line with our aim to support the progress of low-income families, facilitating the empowerment of women and the youth, and leading the transformation of ideas for small projects into key economic activities,” said Mr. Dahlawi.</p>
			  <p>He expressed his elation in welcoming Ebdaa Bank for Micro Finance to the SRB’s list of investors, institutions, Islamic banks and Takaful companies. “We are committed to working with Ebdaa Bank to achieve the common objectives of both parties,” he said.
                The Shariyah Review Bureau (SRB) is the only Shariah advisory firm in Bahrain licensed by the Central Bank of Bahrain. “Our services and approach has helped our firm succeed by building a global platform that covers over 17 countries and helps improve the services of the Sharia Supervisory Board,” he concluded.
