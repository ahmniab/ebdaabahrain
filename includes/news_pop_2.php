               <div class="box_shadow" >
			   <p>Ebdaa Bank reported net profits exceeding BD 306,000 for the financial year ended December 31, 2018, compared to a profit of BD 94,000 in 2017. The increase in net profits is reported to be due to the increase in the volume of operations apart from the number and value of disbursed loans. Operating as a social business, Ebdaa Bank confirmed that profits will be re-injected into the bank’s capital to increase the number and value of low-income loans, helping more Bahrainis to start and/or improve their micro and small businesses and serving the national economy in accordance with the Bahrain 2030 Vision.</p>
			   <p>In its statement on the performance of 2018, Ebdaa Bank reported that its assets increased by over 24% from around BD 2.45 million in 2017 to over BD 3.05 million at the end of 2018. The net loan portfolio increased from BD 1.6 million in 2017 to approximately BD 2.1 million in 2018, an increase of 31%. While the bank’s operational income increased by 40% in 2018, the Bank managed to control its operating expenses, which only increased by 10.7% in 2018, compared to 2017.</p>
			   <p>Ebdaa Bank successfully recovered nearly BD 64,000 from the provision for doubtful debts, as a result of increased efforts in the collection of outstanding loans and arrears from previous years.</p>
