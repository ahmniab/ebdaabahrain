      					  <h1>HISTORY</h1>
      					  <hr>
      				    <p>Ebdaa Bank was established in 2009 as the first microfinance bank in the Kingdom of Bahrain thanks to the support of H.R.H Princess Sabeeka Bint Ibrahim Al Khalifa, the wife of the King of Bahrain and the President of the Supreme Council for Women, may God protect her, as well as the Late Prince Talal Bin Abdul Aziz Al Saud, the President of the Arab Gulf Programme for Development (AGFUND). </p>
      				    <p>The bank’s prime objective is to support low-income families and contribute to socio-economic empowerment by providing microcredit opportunities to loan-income Bahrainis. </p>
      					  <p>Ebdaa Bank serves as a resort for families in need, to improve their overall quality of life. The bank's focus is to assist Bahraini women and youth to ensure their dreams and ideas turn into successful micro-projects. </p>
						<p>The bank also manages the special portfolio of H.R.H. Princess Sabeeka Bint Ibrahim Al Khalifa to support and empower women, in cooperation with the Supreme Council for Women and the Labor Fund "Tamkeen". </p>
						<p>Ebdaa Bank disburses its loans using both Sharia Compliant and Conventional approaches based on each client's preference. In May of 2017, Ebdaa Bank won the “Best Women Empowerment” Award in Dubai in recognition of Ebdaa’s role in supporting and empowering Bahraini women.</p>
