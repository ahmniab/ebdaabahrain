			   <p>Mrs. Al Moayyed expressed her appreciation for Bahrain’s leadership in its efforts to serve low-income clients. “The shareholders and board of directors would like to express our gratitude to the Bahraini businessmen and businesswomen who have provided the bank with interest-free financing this year,” she said.</p>
			   <p>In her statement, Mrs. Al Moayyed appreciated the CBB’s support of the microfinance industry in the Kingdom of Bahrain and its recent decision to reduce the minimum capital of microfinance banks from BD 5 million to BD 2 million, thereby expanding the microfinance base, from BD 5000 to BD 7000. “The Tamkeen Labor Fund has also contributed to our success and has provided continued support to Ebdaa Bank’s programs, performance and staff, particularly in the areas of technical development, training and payment support,” she stated.</p>
			   <p>“We look forward to working with Tamkeen this year on initiatives related to expanding the base of female borrowers, by providing the loan portfolio with a capital grant of BD 500,000, and launching the first phase of the Youth Support Program with a similar amount,” she revealed.</p>
			   <p>Chief Executive Officer of Ebdaa Bank, Dr. Khalid Al-Gazawi stated that the fruitful results achieved over the past four years by Ebdaa Bank are mainly due to the implementation of AGFUND methodologies and standards in the field of microfinance. “Ebdaa Bank has become an example in the success of microfinance institutions in Bahrain and the rest of the region,” said Dr. Al- Ghazzawi.</p>
			   <p>“We would also like to recognize the efforts of and thank our staff at different locations for their outstanding efforts in developing the Bank’s performance,” he added.</p>
			   <p>Ebdaa Bank was able to disburse over 2,600 loans to its new and repeat customers, amounting to over BD 2.6 million, in 2018. “The bank’s plan for the next five years focuses on serving 10,000 borrowers including existing and new customers with loans worth over BD 10 million,” he stated.</p>
