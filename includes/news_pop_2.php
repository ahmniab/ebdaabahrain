			  <p>CEO of Ebdaa Bank for Microfinance, Dr. Khalid Al Ghazzawi, praised the directives of HRH and confirmed the preparedness of Ebdaa Bank, supported by the Arab Gulf Development Program (AGFUND), to create the guide and assist home-based entrepreneurs in effectively utilizing it. A large segment of Ebdaa Bank’s clients are home-based entrepreneurs who borrow funds to grow their projects.</p>
